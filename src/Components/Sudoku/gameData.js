function isReadOnly(row, col) {
    return initialData[row][col] != 0;
}

function clearData(){
    for(let i = 0; i < 9; i++)
        for(let j = 0; j < 9; j++){
            answer[i][j] = 0;
            errNumber[i][j] = 0;
        }
}

function initGame() {
    clearData();
    return axios.get('http://127.0.0.1:3000/easy')
        .then(resData => {
            resData.data.forEach((rowData, rowIndex) => {
                initialData[rowIndex] = [...rowData];
            });
            solvePuzzle(initialData);
            currentData = resData.data;
            return resData.data;
        })
        .catch(function (error) {
            console.log(error);
        });

}

